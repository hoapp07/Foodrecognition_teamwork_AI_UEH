def predict():
    try:
        data = request.json
        arr = preprocess(data['image'])
        preds = model.predict(arr, verbose=0)[0]
        top_idx = int(np.argmax(preds))
        confidence = float(preds[top_idx])
        top3 = [{"name": class_labels[int(i)], "confidence": float(preds[int(i)])}
                for i in np.argsort(preds)[-5:][::-1]]
        return jsonify({
            "name": class_labels[top_idx] if confidence >= THRESHOLD else "Không nhận ra",
            "confidence": confidence,
            "is_known": confidence >= THRESHOLD,
            "top3": top3
        })
    except Exception as e:
        return jsonify({"error": str(e)}), 500

